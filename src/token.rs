#[derive(Debug)]
#[derive(Clone)]
#[derive(PartialEq, Eq)]
pub enum Token {
    If,
    Then,
    Else,
    While,
    Do,
    Skip,
    Id(String),
    Plus,
    Minus,
    Asterisk,
    True,
    False,
    LessThan,
    Equals,
    And,
    Or,
    Not,
    Num(String),
    LeftParenthesis,
    RightParenthesis,
    LeftCurly,
    RightCurly,
    Semicolon,
    Assignment,
    Dollar,
}
